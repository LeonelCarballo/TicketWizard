    private void cargarEventos() {
        List<Evento> eventos = eventoControl.obtenerEventos();
        DefaultTableModel modelo = (DefaultTableModel)this.tblEventos.getModel();
        modelo.setRowCount(0); // Limpia la tabla
        
        for (Evento evento : eventos) {
            modelo.addRow(new Object[]{
                evento.getIdEvento(),
                evento.getNombre(),
                evento.getFechaHora(),
                evento.getNombreLocal()
            });
        }
    }
    
